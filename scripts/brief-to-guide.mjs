// Maps a GuideBrief (src/schemas/guideSchema.ts — AI-generated via Gemini) into the
// site's real sections-array guide shape (src/content.config.ts). Pure function: no
// fs, no network. Country facts (currency/coords/ISO) come from src/data/countries.mjs
// — the SAME authoritative source scripts/scaffold-guide.mjs uses — never from the
// AI's own guess, whenever the country is in that curated list.
//
// Every generated guide lands as draft:true with an explicit ⚠ AI-provenance stamp
// (matching scaffold-guide.mjs's DRAFT_STAMP convention) — nothing here is treated as
// verified until a human runs the research/verification pass (see CLAUDE.md) and
// graduates it via scripts/graduate-guide.mjs.

import { countryData, isoCodeFor } from "../src/data/countries.mjs";

const GEMINI_DRAFT_STAMP =
  "⚠ AI-generated draft (Gemini) — content was generated by a language model and is NOT " +
  "verified. Every price, hour, address, and fact must be checked against a primary " +
  "source before this guide graduates (see CLAUDE.md).";

const P = (group, title, extra = {}) => ({ type: "panel", group, title, body: "", ...extra });

export function buildGuideFromBrief(brief, country) {
  const data = countryData(country);
  const iso = isoCodeFor(country);
  const coords = data?.capital || null;
  const sections = [];

  // Plan — overview + country-driven live sections (only when data is real, never invented)
  sections.push({
    type: "infogrid", group: "Plan", title: "Overview",
    cards: [
      { icon: "💰", label: "Cost tier", body: brief.cost },
      { icon: "✨", label: "Vibe", body: brief.vibe },
    ],
  });
  if (coords) sections.push({ type: "weather", group: "Plan", title: "Weather for your dates" });
  if (iso)    sections.push({ type: "holidays", group: "Plan", title: "Public holidays & closures" });

  // Money & budget — authoritative currency symbol/code/name from countryData when known;
  // the AI's exchange_advice is genuinely AI-authored content, kept as-is but unverified.
  const currencySym  = data?.currency?.symbol || brief.currency.code || "$";
  const currencyName = data?.currency?.name   || brief.currency.name;
  const currencyCode = data?.currency?.code   || brief.currency.code;
  const currencyNote = data
    ? `<p><b>${currencyName} (${currencyCode}).</b> ${brief.currency.exchange_advice}</p>`
    : `<p><b>${currencyName} (${currencyCode}).</b> ${brief.currency.exchange_advice} ` +
      `<i>⚠ Currency code/name not cross-checked against site data — country not in the curated list.</i></p>`;
  sections.push({ type: "prose", group: "Money & budget", title: "Money & currency", body: currencyNote });
  sections.push({
    type: "budget", group: "Money & budget", title: "Budget & daily costs",
    intro: `Rough per-person estimates for a ${brief.cost} trip — replace the zeros with researched figures.`,
    currency: currencySym,
    items: [
      { label: "Lodging, per night", basis: "day", est: 0 },
      { label: "Food & drink, per day", basis: "day", est: 0 },
      { label: "Local transport, per day", basis: "day", est: 0 },
      { label: "Sights & activities, per day", basis: "day", est: 0 },
      { label: "Flights — round trip", basis: "trip", est: 0 },
    ],
  });

  // Etiquette & language
  sections.push(P("Etiquette & language", "Etiquette & culture tips", { checklist: brief.culture_tips }));

  // Sights — infogrid, NOT the `sights` type: CLAUDE.md requires an exact verified
  // Wikimedia Commons filename for sights.img.file, which an LLM cannot supply.
  sections.push({
    type: "infogrid", group: "Sights", title: "Must-do activities",
    cards: brief.must_do_activities.map((a) => ({
      label: a.name,
      body: a.description + (a.booking_required ? " <b>⚠ Advance booking typically required.</b>" : ""),
    })),
  });

  // Food & shopping — restaurants (optional). The brief has no address/transit field at
  // all, so every card is explicitly flagged as missing the info CLAUDE.md's 4-question
  // restaurant standard requires (where / how to get there / when / book), rather than
  // fabricating it.
  if (brief.restaurants?.length) {
    sections.push({
      type: "infogrid", group: "Food & shopping", title: "Restaurants to consider",
      note: "⚠ AI-suggested — none of these have a verified address, transit route, hours, or booking status yet.",
      cards: brief.restaurants.map((r) => ({
        icon: "🍽️", label: `${r.name} (${r.price_tier})`,
        body: `${r.cuisine} — ${r.why_go} <b>⚠ address, transit route, and booking status not yet verified — required before this guide graduates.</b>`,
      })),
    });
  }

  // Health & safety — emergency contacts (optional), tel: link markup matches the
  // existing pattern used across the site's real guides (e.g. korea.json).
  if (brief.emergency_contacts?.length) {
    const body = "<p>" + brief.emergency_contacts
      .map((c) => `<b>${c.service}:</b> <a href='tel:${c.number}'><b>${c.number}</b></a>`)
      .join(". ") + ".</p>";
    sections.push(P("Health & safety", "Emergency contacts", { body }));
  }

  // Getting around — orientation map, only when real capital coords exist
  if (coords) {
    sections.push({
      type: "map", group: "Getting around", title: "Orientation map",
      center: { lat: coords.lat, lng: coords.lng }, span: 0.08,
    });
  }

  // References — canonical closing section; a human fills sources during verification
  sections.push({ type: "prose", group: "References", title: "Sources & further reading", body: "" });

  return {
    kicker: "AI draft",
    title: brief.title,
    dek: "",
    country,
    draft: true,
    verified: GEMINI_DRAFT_STAMP,
    footer: "",
    sections,
  };
}
