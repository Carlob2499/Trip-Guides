// Rewrites the service worker's CORE precache list in dist/ from the guides
// that ACTUALLY built (replaces the hand-maintained list in public/sw.js —
// which stays as the committed fallback). Runs as part of `npm run build`
// (see package.json), after `astro build`, before the perf budget.
//
// Why: a hardcoded list silently drifts — a new guide ships without offline
// support; a removed one 404s during SW install (cache.add is per-URL guarded,
// but the miss is still a silent gap). Deriving from dist/ makes the precache
// exactly the set of pages that exist, every build, with no memory required.

import { readdir, readFile, writeFile, access } from "node:fs/promises";
import { createHash } from "node:crypto";
import path from "node:path";

const DIST = "dist";
const SW = path.join(DIST, "sw.js");
const BEGIN = "/* CORE:BEGIN";
const END = "/* CORE:END */";

// Static shell entries beyond the guide pages (mirror public/ assets).
const SHELL = ["/", "/manifest.webmanifest", "/icons/icon-192.png", "/icons/favicon.svg"];

try { await access(SW); } catch {
  console.error("[sw-precache] dist/sw.js not found — run astro build first");
  process.exit(1);
}

// Every built guide page: dist/guides/<slug>/index.html
let slugs = [];
try {
  const entries = await readdir(path.join(DIST, "guides"), { withFileTypes: true });
  for (const e of entries) {
    if (!e.isDirectory()) continue;
    try { await access(path.join(DIST, "guides", e.name, "index.html")); slugs.push(e.name); } catch {}
  }
} catch {}
slugs.sort();

const src = await readFile(SW, "utf8");
const b = src.indexOf(BEGIN);
const e = src.indexOf(END);
if (b === -1 || e === -1) {
  console.error("[sw-precache] CORE:BEGIN/END markers not found in dist/sw.js — public/sw.js drifted; not touching it");
  process.exit(1);
}

const lines = [
  ...SHELL.map((p) => `  BASE + ${JSON.stringify(p)},`),
  ...slugs.map((s) => `  BASE + ${JSON.stringify(`/guides/${s}/`)},`),
];
const block =
  `${BEGIN} — GENERATED by scripts/gen-sw-precache.mjs from the built guides. */\n` +
  `const CORE = [\n${lines.join("\n")}\n];\n${END}`;

let out = src.slice(0, b) + block + src.slice(e + END.length);

// Auto-version the cache from a content hash of everything precached, so any shipped
// JS/CSS/HTML change ALWAYS invalidates returning users' caches — no manual `CACHE` bump
// (the old footgun: forget it once and returning devices run stale code). Astro
// content-hashes every asset FILENAME, so the sorted _astro listing captures all JS/CSS
// changes; the guide/shell HTML captures content changes and which hashed assets they pull.
const h = createHash("sha256");
try { h.update((await readdir(path.join(DIST, "_astro"))).sort().join("\n")); } catch {}
for (const rel of ["index.html", ...slugs.map((s) => path.join("guides", s, "index.html"))]) {
  try { h.update(await readFile(path.join(DIST, rel))); } catch {}
}
const hash = h.digest("hex").slice(0, 12);

const versioned = out.replace(/const CACHE = "tripguides-[^"]*";/, `const CACHE = "tripguides-${hash}";`);
if (versioned === out) {
  console.error('[sw-precache] `const CACHE = "tripguides-..."` line not found in dist/sw.js — cannot auto-version');
  process.exit(1);
}

await writeFile(SW, versioned, "utf8");
console.log(`[sw-precache] CORE rewritten: ${SHELL.length} shell + ${slugs.length} guide page(s) — ${slugs.join(", ") || "none"} · cache tripguides-${hash}`);
